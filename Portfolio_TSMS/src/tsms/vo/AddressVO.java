package tsms.vo;

public class AddressVO {
	protected int num;
	protected String si_do;
	protected String si_gun_gu;
	
	public int getNum() {
		return num;
	}
	public AddressVO setNum(int num) {
		this.num = num;
		return this;
	}
	public String getSi_do() {
		return si_do;
	}
	public AddressVO setSi_do(String si_do) {
		this.si_do = si_do;
		return this;
	}
	public String getSi_gun_gu() {
		return si_gun_gu;
	}
	public AddressVO setSi_gun_gu(String si_gun_gu) {
		this.si_gun_gu = si_gun_gu;
		return this;
	}
	
	
}
