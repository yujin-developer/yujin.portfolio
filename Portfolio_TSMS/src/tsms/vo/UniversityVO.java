package tsms.vo;

public class UniversityVO {
	protected int num;
	protected String university;
	
	public int getNum() {
		return num;
	}
	public UniversityVO setNum(int num) {
		this.num = num;
		return this;
	}
	public String getUniversity() {
		return university;
	}
	public UniversityVO setUniversity(String university) {
		this.university = university;
		return this;
	}
	
	
}
